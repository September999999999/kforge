function reviewQueueSuggestedCommands(item: ReviewQueueItem): string[] {
  const commands = [`kforge inspect . --file ${shellQuote(item.file)}`];

  if (item.blockers.length > 0) {
    commands.push("kforge doctor . --write");
    return commands;
  }

  if (item.kind === "compile" && item.status !== "accepted" && !item.sources.some(isExternalRef)) {
    const source = firstLocalSource(item.sources);
    const target = item.targets[0];
    if (source && target) {
      commands.push(`kforge inspect . --file ${shellQuote(source)}`);
      commands.push(`kforge compile draft . --review ${shellQuote(item.file)} --write`);
      commands.push(`kforge review content . --file ${shellQuote(item.file)} --from outputs/<draft>.md`);
    }
  }

  if (item.status === "accepted") {
    commands.push(`kforge review apply . --file ${shellQuote(item.file)} --dry-run`);
  }

  if (item.status !== "accepted" && item.status !== "rejected" && item.status !== "applied") {
    commands.push(`kforge review status . --file ${shellQuote(item.file)} --status accepted --note "Checked source and target."`);
  }

  return commands;
}

