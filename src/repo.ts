export function seedTasks(repoPath: string, options: TaskSeedOptions = {}): CommandResult {
  requireRepo(repoPath);
  mkdirSyncish(path.join(repoPath, "tasks"));

  const candidates = reviewQueueItems(repoPath)
    .filter((item) => reviewQueueStatusMatches(item, "actionable"))
    .slice(0, options.limit ?? 10);
  const items: TaskSeedItem[] = [];

  for (const candidate of candidates) {
    const existing = taskForSource(repoPath, candidate.file);
    if (existing) {
      items.push({ task: toRepoPath(repoPath, existing), source: candidate.file, action: "skipped_existing" });
      continue;
    }

    const taskPath = nextAvailableTaskPath(repoPath, candidate.title);
    writeFileSyncish(taskPath, taskTemplate(candidate));
    items.push({ task: toRepoPath(repoPath, taskPath), source: candidate.file, action: "created" });
  }

  const payload = taskSeedPayload(candidates.length, items);
  if (options.json) {
    return { ok: true, messages: [JSON.stringify(payload, null, 2)] };
  }

  const lines = [
    "# Task Seed",
    "",
    `Updated: ${today()}`,
    "",
    `Candidates: ${payload.counts.candidates}`,
    `Created: ${payload.counts.created}`,
    `Skipped existing: ${payload.counts.skippedExisting}`,
    "",
    ...items.map((item) => `- ${item.action}: \`${item.task}\` from \`${item.source}\``),
    ...(items.length === 0 ? ["No actionable reviews to seed."] : []),
    "",
  ];

  return { ok: true, messages: [lines.join("\n")] };
}

