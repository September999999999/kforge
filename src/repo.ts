export function searchRepo(repoPath: string, options: SearchOptions): CommandResult {
  requireRepo(repoPath);

  const terms = searchTerms(options.query);
  if (terms.length === 0) {
    return { ok: false, messages: ["search query is required"] };
  }

  const scopes: SearchScope[] = options.scopes?.length
    ? options.scopes
    : ["raw", "wiki", "claims", "reviews", "outputs"];
  const limit = clamp(options.limit ?? 10, 1, 50);
  const results = scopes
    .flatMap((scope) => searchScope(repoPath, scope, terms))
    .sort((left, right) => right.score - left.score || left.path.localeCompare(right.path))
    .slice(0, limit);

  const payload = searchPayload(options.query, scopes, limit, results);
  if (options.json) {
    return { ok: true, messages: [JSON.stringify(payload, null, 2)] };
  }

  return { ok: true, messages: [searchResultsMarkdown(payload)] };
}

