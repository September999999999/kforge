function outputInspectPayload(repoPath: string, file: string): OutputInspectPayload {
  const rel = toRepoPath(repoPath, file);
  const stats = statSyncish(file);
  const searchable = isSearchableFile(file);
  const text = searchable ? readText(file) : "";
  const referencedBy = filesReferencingSource(repoPath, rel);
  const payload: OutputInspectPayload = {
    ok: true,
    updatedAt: today(),
    output: rel,
    title: searchable ? documentTitle(text, rel) : path.basename(file),
    size: stats.size,
    searchable,
    headings: searchable ? markdownHeadings(text).slice(0, 12) : [],
    sources: searchable ? refsForFileSources(text, rel) : [],
    referencedBy,
    suggestedCommands: [
      `kforge promote . --file ${shellQuote(rel)} --target wiki/<Page>.md --json`,
      `kforge promote . --file ${shellQuote(rel)} --target claims/<claim>.md --json`,
    ],
  };

  if (searchable) {
    const status = metadataField(text, "status");
    const kind = metadataField(text, "kind");
    const created = metadataField(text, "created");
    return {
      ...payload,
      ...(status ? { status } : {}),
      ...(kind ? { kind } : {}),
      ...(created ? { created } : {}),
    };
  }

  return payload;
}

