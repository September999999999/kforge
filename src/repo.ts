function externalSymlinkRefs(repoPath: string): string[] {
  const repoRoot = path.resolve(repoPath);
  const realRepoRoot = realpathSyncish(repoRoot);
  const issues: string[] = [];

  for (const directory of CANONICAL_DIRS) {
    const root = path.join(repoPath, directory);
    if (!exists(root)) {
      continue;
    }
    issues.push(...externalSymlinkRefsIn(repoPath, root, realRepoRoot));
  }

  return [...new Set(issues)].sort();
}

function externalSymlinkRefsIn(repoPath: string, root: string, realRepoRoot: string): string[] {
  const issues: string[] = [];

  for (const entry of readdirSyncish(root)) {
    if (entry.name.startsWith(".")) {
      continue;
    }

    const fullPath = path.join(root, entry.name);
    const rel = toRepoPath(repoPath, fullPath);
    const lstats = lstatSyncish(fullPath);
    if (lstats.isSymbolicLink()) {
      const realTarget = realExistingPath(fullPath);
      if (!realTarget) {
        issues.push(`${rel} -> missing target`);
        continue;
      }

      if (!isInsidePath(realRepoRoot, realTarget)) {
        issues.push(`${rel} -> ${realTarget}`);
        continue;
      }
    }

    if (lstats.isDirectory()) {
      issues.push(...externalSymlinkRefsIn(repoPath, fullPath, realRepoRoot));
    }
  }

  return issues;
}

function missingSourceRefs(repoPath: string, owner: string, refs: string[]): string[] {
  const missing: string[] = [];
  for (const ref of refs) {
    if (isExternalRef(ref)) {
      continue;
    }

    const boundaryError = repoPathBoundaryError(repoPath, ref);
    if (boundaryError) {
      missing.push(`${owner} -> ${ref} (${boundaryError})`);
      continue;
    }

    const target = path.resolve(repoPath, ref);
    if (!exists(target)) {
      missing.push(`${owner} -> ${ref}`);
    }
  }
  return missing;
}

function invalidRepoPathRefs(repoPath: string, owner: string, refs: string[]): string[] {
  const invalid: string[] = [];

  for (const ref of refs) {
    if (isExternalRef(ref)) {
      invalid.push(`${owner} -> ${ref} (external target)`);
      continue;
    }

    const boundaryError = repoPathBoundaryError(repoPath, ref);
    if (boundaryError) {
      invalid.push(`${owner} -> ${ref} (${boundaryError})`);
    }
  }

  return invalid;
}

function repoPathBoundaryError(repoPath: string, ref: string): string | undefined {
  const repoRoot = path.resolve(repoPath);
  const target = path.resolve(repoPath, ref);
  if (!isInsidePath(repoRoot, target)) {
    return "outside repo";
  }

  const realRepoRoot = realpathSyncish(repoRoot);
  const realTarget = realExistingPath(target);
  if (realTarget && !isInsidePath(realRepoRoot, realTarget)) {
    return "outside repo via symlink";
  }

  const realParent = realExistingParent(target);
  if (realParent && !isInsidePath(realRepoRoot, realParent)) {
    return "outside repo via symlink";
  }

  return undefined;
}

function isInsidePath(root: string, target: string): boolean {
  return target === root || target.startsWith(`${root}${path.sep}`);
}

function realExistingPath(target: string): string | undefined {
  try {
    return realpathSyncish(target);
  } catch {
    return undefined;
  }
}

function realExistingParent(target: string): string | undefined {
  let current = path.dirname(target);
  const parsed = path.parse(current);

  while (current && current !== parsed.root) {
    const real = realExistingPath(current);
    if (real) {
      return real;
    }
    current = path.dirname(current);
  }

  return realExistingPath(parsed.root);
}

function frontmatterSourceRefs(text: string): string[] {
  const frontmatter = text.match(/^---\n([\s\S]*?)\n---/)?.[1];
  if (!frontmatter) {
    return [];
  }

  const lines = frontmatter.split("\n");
  const refs: string[] = [];

  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index];
    const inlineSources = line.match(/^sources:\s*\[(.*)\]\s*$/);
    if (inlineSources) {
      refs.push(...inlineSources[1].split(",").map(normalizeRepoRef).filter(Boolean));
      continue;
    }

    if (!line.match(/^sources:\s*$/)) {
      continue;
    }

    for (let child = index + 1; child < lines.length; child += 1) {
      const childLine = lines[child];
      if (!childLine.startsWith(" ")) {
        break;
      }
      const source = childLine.match(/^\s*-\s*(.+?)\s*$/)?.[1];
      if (source) {
        refs.push(normalizeRepoRef(source));
      }
    }
  }

  return refs.filter(Boolean);
}

function sourceRefs(text: string): string[] {
  return markdownListRefs(text, "Sources");
}

function reviewTargetRefs(text: string): string[] {
  return markdownListRefs(text, "Targets");
}

function markdownListRefs(text: string, heading: "Sources" | "Targets"): string[] {
  const section = markdownSection(text, heading);
  if (!section) {
    return [];
  }

  return section
    .split("\n")
    .map((line) => line.match(/^\s*-\s*(.+?)\s*$/)?.[1] ?? "")
    .map(normalizeRepoRef)
    .filter(Boolean);
}

