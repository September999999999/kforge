type ReviewArtifactRead =
  | { ok: true; fileRef: string; filePath: string; text: string }
  | { ok: false; result: CommandResult };

function readReviewArtifact(repoPath: string, file: string, owner: string): ReviewArtifactRead {
  const fileRef = normalizeRepoRef(file);
  if (!fileRef) {
    return { ok: false, result: { ok: false, messages: ["review file is required"] } };
  }

  if (!fileRef.startsWith("reviews/")) {
    return {
      ok: false,
      result: { ok: false, messages: [`${owner} can only use files under reviews/: ${fileRef}`] },
    };
  }

  const invalidRefs = invalidRepoPathRefs(repoPath, owner, [fileRef]);
  if (invalidRefs.length > 0) {
    return {
      ok: false,
      result: {
        ok: false,
        messages: [`${owner} path check failed:`, ...invalidRefs.map((item) => `  - ${item}`)],
      },
    };
  }

  const filePath = path.resolve(repoPath, fileRef);
  if (!exists(filePath)) {
    return { ok: false, result: { ok: false, messages: [`review file not found: ${fileRef}`] } };
  }

  if (!statSyncish(filePath).isFile()) {
    return { ok: false, result: { ok: false, messages: [`review target is not a file: ${fileRef}`] } };
  }

  return { ok: true, fileRef, filePath, text: readText(filePath) };
}

function reviewContentFromFile(
  repoPath: string,
  reviewFile: string,
  fromRef: string,
): { ok: true; content: string; source: string } | { ok: false; result: CommandResult } {
  if (!fromRef) {
    return { ok: false, result: { ok: false, messages: ["review content source file is required"] } };
  }

  const invalidRefs = invalidRepoPathRefs(repoPath, "review content", [fromRef]);
  if (invalidRefs.length > 0) {
    return {
      ok: false,
      result: {
        ok: false,
        messages: ["review content source path check failed:", ...invalidRefs.map((item) => `  - ${item}`)],
      },
    };
  }

  const sourcePath = path.resolve(repoPath, fromRef);
  if (!exists(sourcePath)) {
    return { ok: false, result: { ok: false, messages: [`review content source file not found: ${fromRef}`] } };
  }

  if (!statSyncish(sourcePath).isFile()) {
    return { ok: false, result: { ok: false, messages: [`review content source is not a file: ${fromRef}`] } };
  }

  if (!isSearchableFile(sourcePath)) {
    return { ok: false, result: { ok: false, messages: [`review content source is not a supported text file: ${fromRef}`] } };
  }

  const content = readText(sourcePath).trim();
  if (!content) {
    return { ok: false, result: { ok: false, messages: [`review content source is empty: ${fromRef}`] } };
  }

  if (sameRepoRef(fromRef, reviewFile)) {
    return { ok: false, result: { ok: false, messages: ["review content source cannot be the review file itself"] } };
  }

  return { ok: true, content, source: fromRef };
}

function documentField(text: string, field: string): string | undefined {
  return text.match(new RegExp(`^${field}:\\s*(.+?)\\s*$`, "m"))?.[1]?.trim();
}

function setDocumentField(text: string, field: string, value: string): string {
  const fieldPattern = new RegExp(`^(${escapeRegExp(field)}:\\s*).*$`, "m");
  if (fieldPattern.test(text)) {
    return text.replace(fieldPattern, `$1${value}`);
  }

  const lines = text.split(/\r?\n/);
  const headingIndex = lines.findIndex((line) => line.match(/^#\s+/));
  if (headingIndex >= 0) {
    lines.splice(headingIndex + 1, 0, "", `${field}: ${value}`);
    return lines.join("\n");
  }

  return `${field}: ${value}\n${text}`;
}

