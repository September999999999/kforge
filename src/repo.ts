type CompileDraftInput =
  | { ok: true; sources: string[]; target: string; title: string; review?: string }
  | { ok: false; result: CommandResult };

function compileDraftInput(repoPath: string, options: CompileDraftOptions): CompileDraftInput {
  if (options.review) {
    return compileDraftInputFromReview(repoPath, options);
  }

  const sources = [...new Set((options.sources ?? []).map(normalizeRepoRef).filter(Boolean))];
  const target = normalizeRepoRef(options.target ?? "");
  return validateCompileDraftInput(repoPath, sources, target, options.title);
}

function compileDraftInputFromReview(repoPath: string, options: CompileDraftOptions): CompileDraftInput {
  const reviewOrError = readReviewArtifact(repoPath, options.review ?? "", "compile draft");
  if (!reviewOrError.ok) {
    return { ok: false, result: reviewOrError.result };
  }

  const text = reviewOrError.text;
  const kind = documentField(text, "Kind") ?? "unknown";
  if (kind !== "compile") {
    return { ok: false, result: { ok: false, messages: [`compile draft requires a compile review; found kind ${kind}`] } };
  }

  const targets = reviewTargetRefs(text);
  if (targets.length !== 1) {
    return { ok: false, result: { ok: false, messages: [`compile draft requires exactly one review target; found ${targets.length}`] } };
  }

  const sources = sourceRefs(text).filter((source) => !isExternalRef(source));
  if (sources.length === 0) {
    return { ok: false, result: { ok: false, messages: ["compile draft requires at least one local review source"] } };
  }

  return validateCompileDraftInput(repoPath, sources, targets[0], options.title, reviewOrError.fileRef);
}

function validateCompileDraftInput(
  repoPath: string,
  sources: string[],
  target: string,
  title: string | undefined,
  review?: string,
): CompileDraftInput {
  if (!target) {
    return { ok: false, result: { ok: false, messages: ["compile draft target is required"] } };
  }

  if (sources.length === 0) {
    return { ok: false, result: { ok: false, messages: ["compile draft requires at least one --source or --review"] } };
  }

  const invalidTarget = invalidRepoPathRefs(repoPath, "compile draft target", [target]);
  if (invalidTarget.length > 0) {
    return { ok: false, result: { ok: false, messages: ["compile draft target check failed:", ...invalidTarget.map((item) => `  - ${item}`)] } };
  }

  if (!target.startsWith("wiki/")) {
    return { ok: false, result: { ok: false, messages: [`compile draft target must be under wiki/: ${target}`] } };
  }

  const invalidSources = invalidRepoPathRefs(repoPath, "compile draft source", sources);
  if (invalidSources.length > 0) {
    return { ok: false, result: { ok: false, messages: ["compile draft source path check failed:", ...invalidSources.map((item) => `  - ${item}`)] } };
  }

  const missingSources = missingSourceRefs(repoPath, "compile draft source", sources);
  if (missingSources.length > 0) {
    return { ok: false, result: { ok: false, messages: ["compile draft source check failed:", ...missingSources.map((item) => `  - ${item}`)] } };
  }

  const sourceNotFiles = sources.filter((source) => !statSyncish(path.resolve(repoPath, source)).isFile());
  if (sourceNotFiles.length > 0) {
    return { ok: false, result: { ok: false, messages: ["compile draft sources must be files:", ...sourceNotFiles.map((item) => `  - ${item}`)] } };
  }

  return {
    ok: true,
    sources,
    target,
    title: title?.trim() || titleFromTarget(target),
    review,
  };
}

function sourceEvidencePrompts(repoPath: string, source: string): string[] {
  const sourcePath = path.resolve(repoPath, source);
  const metadata = readRawSourceMetadata(repoPath, sourcePath);
  const lines = [`- \`${source}\`${metadata?.title ? ` (${metadata.title})` : ""}: TODO: summarize relevant evidence.`];
  if (metadata?.url) {
    lines.push(`  - URL: ${metadata.url}`);
  }
  return lines;
}

function compileDraftSourceMetadataLines(repoPath: string, source: string): string[] {
  const sourcePath = path.resolve(repoPath, source);
  const metadata = readRawSourceMetadata(repoPath, sourcePath);
  if (!metadata) {
    return [`### \`${source}\``, "", "- metadata: not found"];
  }

  const lines = [
    `### \`${source}\``,
    "",
    `- metadata: \`${metadata.metadataRef}\``,
  ];
  pushOptionalDraftMetadata(lines, "title", metadata.title);
  pushOptionalDraftMetadata(lines, "url", metadata.url);
  pushOptionalDraftMetadata(lines, "author", metadata.author);
  pushOptionalDraftMetadata(lines, "date", metadata.date);
  pushOptionalDraftMetadata(lines, "license", metadata.license);
  pushOptionalDraftMetadata(lines, "original_path", metadata.originalPath);
  if (metadata.notes?.trim()) {
    lines.push("- notes:", ...metadata.notes.trim().split("\n").map((line) => `  ${line}`));
  }
  return lines;
}

function pushOptionalDraftMetadata(lines: string[], label: string, value: string | undefined): void {
  if (value?.trim()) {
    lines.push(`- ${label}: ${value.trim()}`);
  }
}

function compileDraftSourceExcerptLines(repoPath: string, source: string): string[] {
  const excerpt = sourceExcerpt(repoPath, source);
  const lines = [`### \`${source}\``, "", `- embedded: ${excerpt.embedded ? "yes" : "no"}`];
  if (!excerpt.excerpt) {
    lines.push("- excerpt: not embedded because this source is not a supported text file");
    return lines;
  }

  lines.push("", fencedBlock(excerpt.language, excerpt.excerpt.text));
  if (excerpt.excerpt.truncated) {
    lines.push("", "- excerpt was truncated");
  }
  return lines;
}

function compileDraftExistingTargetLines(repoPath: string, target: string): string[] {
  const targetPath = path.resolve(repoPath, target);
  if (!exists(targetPath)) {
    return [`- target: \`${target}\``, "- exists: no"];
  }

  if (!statSyncish(targetPath).isFile()) {
    return [`- target: \`${target}\``, "- exists: yes", "- file: no"];
  }

  if (!isSearchableFile(targetPath)) {
    return [`- target: \`${target}\``, "- exists: yes", "- searchable text: no"];
  }

  const excerpt = trimForBrief(readText(targetPath), 4000);
  const lines = [
    `- target: \`${target}\``,
    "- exists: yes",
    "- searchable text: yes",
    "",
    fencedBlock("markdown", excerpt.text),
  ];
  if (excerpt.truncated) {
    lines.push("", "- existing target excerpt was truncated");
  }
  return lines;
}

interface BriefExcerpt {
  text: string;
  truncated: boolean;
}

interface SourceExcerpt {
  path: string;
  size: number;
  embedded: boolean;
  language: string;
  excerpt?: BriefExcerpt;
}

function sourceExcerpt(repoPath: string, source: string): SourceExcerpt {
  const file = path.resolve(repoPath, source);
  const stats = statSyncish(file);
  const language = fenceLanguage(file);
  if (!isSearchableFile(file)) {
    return {
      path: source,
      size: stats.size,
      embedded: false,
      language,
    };
  }

  return {
    path: source,
    size: stats.size,
    embedded: true,
    language,
    excerpt: trimForBrief(readText(file), 8000),
  };
}

function trimForBrief(text: string, maxChars: number): BriefExcerpt {
  const normalized = text.trim();
  if (normalized.length <= maxChars) {
    return { text: normalized, truncated: false };
  }

  return { text: normalized.slice(0, maxChars).trimEnd(), truncated: true };
}

function fencedBlock(language: string, text: string): string {
  const longestRun = Math.max(0, ...[...text.matchAll(/`+/g)].map((match) => match[0].length));
  const fence = "`".repeat(Math.max(4, longestRun + 1));
  return `${fence}${language}\n${text}\n${fence}`;
}

function fenceLanguage(file: string): string {
  const extension = path.extname(file).toLowerCase().replace(/^\./, "");
  if (extension === "markdown") {
    return "markdown";
  }
  if (extension === "md") {
    return "markdown";
  }
  if (extension === "txt") {
    return "text";
  }
  if (!extension) {
    return "text";
  }
  return extension;
}

function bestSnippet(text: string, terms: string[]): string {
  const lines = text
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean);

  if (lines.length === 0) {
    return "";
  }

  const ranked = lines
    .map((line) => ({ line, score: occurrenceCount(line.toLowerCase(), terms) }))
    .sort((left, right) => right.score - left.score);

  const best = ranked[0]?.line ?? lines[0];
  return best.length > 160 ? `${best.slice(0, 157)}...` : best;
}

function occurrenceCount(text: string, terms: string[]): number {
  let total = 0;
  for (const term of terms) {
    total += text.split(term).length - 1;
  }
  return total;
}

