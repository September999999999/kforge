export function agentPlan(repoPath: string, options: AgentPlanOptions): CommandResult {
  requireRepo(repoPath);

  const agents = [...new Set(options.agents.map((agent) => agent.trim()).filter(Boolean))];
  if (agents.length === 0) {
    return { ok: false, messages: ["agent plan requires at least one --agent"] };
  }

  let seeded = taskSeedPayload(0, []);
  if (options.seed !== false) {
    const seedResult = seedTasks(repoPath, { limit: agentPlanSeedLimit(repoPath, agents.length, options.limit), json: true });
    if (!seedResult.ok) {
      return seedResult;
    }
    seeded = JSON.parse(seedResult.messages[0]) as TaskSeedPayload;
  }

  const assignments: AgentPlanAssignment[] = [];
  const unassignedAgents: string[] = [];

  for (const agent of agents) {
    const result = nextRun(repoPath, {
      agent,
      seed: false,
      limit: options.limit,
      note: options.note,
      json: true,
    });
    if (!result.ok) {
      return result;
    }

    const payload = JSON.parse(result.messages[0]) as RunNextPayload;
    if (!payload.task || !payload.run) {
      unassignedAgents.push(agent);
      continue;
    }

    assignments.push({
      agent,
      task: payload.task,
      run: payload.run,
      read: agentStepReadRefs(payload.run, payload.task),
      commands: agentStepCommands(payload.run, payload.task),
      finish: [`kforge agent finish . --agent ${shellQuote(agent)} --run ${shellQuote(payload.run.file)} --status success --task-done --json`],
      next: [`kforge agent step . --agent ${shellQuote(agent)} --json`, `kforge run inspect . --run ${shellQuote(payload.run.file)} --json`],
    });
  }

  const payload: AgentPlanPayload = {
    ok: true,
    updatedAt: today(),
    requested: agents.length,
    started: assignments.length,
    seeded,
    assignments,
    unassignedAgents,
    next:
      unassignedAgents.length > 0
        ? ["kforge review queue . --json", "kforge task seed . --json"]
        : assignments.map((assignment) => `kforge agent step . --agent ${shellQuote(assignment.agent)} --json`),
  };

  if (options.json) {
    return { ok: true, messages: [JSON.stringify(payload, null, 2)] };
  }

  const lines = [
    "# Agent Parallel Plan",
    "",
    `Updated: ${today()}`,
    `Requested agents: ${payload.requested}`,
    `Started runs: ${payload.started}`,
    `Unassigned agents: ${payload.unassignedAgents.length}`,
    "",
    ...(assignments.length > 0
      ? [
          "| Agent | Task | Run | Next |",
          "| --- | --- | --- | --- |",
          ...assignments.map(
            (assignment) =>
              `| ${escapeTableCell(assignment.agent)} | \`${assignment.task.file}\` | \`${assignment.run.file}\` | \`${assignment.next[0]}\` |`,
          ),
        ]
      : ["No agent runs were started."]),
    "",
    ...(unassignedAgents.length > 0
      ? ["## Unassigned", "", ...unassignedAgents.map((agent) => `- ${agent}`), "", "Run `kforge review queue .` or `kforge task seed .` to create more work."]
      : []),
  ];

  return { ok: true, messages: [lines.join("\n")] };
}

function agentPlanSeedLimit(repoPath: string, agentCount: number, requestedLimit: number | undefined): number {
  const baseLimit = Math.max(requestedLimit ?? agentCount, agentCount);
  const existingTasks = taskItems(repoPath);
  const openTasks = existingTasks.filter((task) => task.status === "open").length;
  const neededNewTasks = Math.max(agentCount - openTasks, 0);
  if (neededNewTasks === 0) {
    return baseLimit;
  }

  const existingSources = new Set(existingTasks.map((task) => task.source));
  const candidates = reviewQueueItems(repoPath).filter((item) => reviewQueueStatusMatches(item, "actionable"));
  let scanLimit = 0;
  let seedable = 0;
  for (const candidate of candidates) {
    scanLimit += 1;
    if (!existingSources.has(candidate.file)) {
      seedable += 1;
    }
    if (seedable >= neededNewTasks) {
      break;
    }
  }
  return Math.max(baseLimit, scanLimit);
}

export function agentLaunch(repoPath: string, options: AgentLaunchOptions): CommandResult {
  requireRepo(repoPath);

  const agents = [...new Set(options.agents.map((agent) => agent.trim()).filter(Boolean))];
  if (agents.length === 0) {
    return { ok: false, messages: ["agent launch requires at least one --agent"] };
  }

  const commandTemplate = options.command?.trim() || "kforge agent step . --agent {agent} --json";
  if (!commandTemplate) {
    return { ok: false, messages: ["agent launch command template cannot be empty"] };
  }

  let planned: AgentPlanPayload | undefined;
  let source: AgentLaunchPayload["source"] = "existing";
  if (!options.noPlan) {
    const planResult = agentPlan(repoPath, {
      agents,
      limit: options.limit,
      note: options.note,
      json: true,
    });
    if (!planResult.ok) {
      return planResult;
    }
    planned = JSON.parse(planResult.messages[0]) as AgentPlanPayload;
    source = "planned";
  }

  const assignments =
    planned?.assignments && planned.assignments.length > 0
      ? planned.assignments
      : agents.flatMap((agent) => currentAgentLaunchAssignment(repoPath, agent));
  const items = assignments.map((assignment) => agentLaunchItem(repoPath, assignment, commandTemplate));
  const scriptContent = agentLaunchScript(repoPath, items);
  let scriptFile: string | undefined;

  if (options.write || options.exec) {
    ensureAgentLaunchLogFiles(repoPath, items);
    scriptFile = writeAgentLaunchScript(repoPath, scriptContent);
  }

  let executed = false;
  let exitCode: number | undefined;
  if (options.exec) {
    if (!scriptFile) {
      return { ok: false, messages: ["agent launch internal error: missing launch script"] };
    }
    const result = spawnSync("bash", [path.resolve(repoPath, scriptFile)], {
      cwd: repoPath,
      stdio: "inherit",
    });
    executed = true;
    exitCode = result.status ?? 1;
  }

  const payload = agentLaunchPayload({
    commandTemplate,
    source,
    planned,
    items,
    scriptContent,
    scriptFile,
    written: Boolean(scriptFile),
    executed,
    exitCode,
  });

  return agentLaunchResult(payload, options.json);
}

export function agentDispatch(repoPath: string, options: AgentDispatchOptions): CommandResult {
  requireRepo(repoPath);

  const agents = [...new Set(options.agents.map((agent) => agent.trim()).filter(Boolean))];
  if (agents.length === 0) {
    return { ok: false, messages: ["agent dispatch requires at least one --agent"] };
  }

  const bootstrapResult = bootstrapRepo(repoPath, {
    agents,
    limit: options.limit,
    note: options.note,
    dryRun: options.dryRun,
    json: true,
  });
  if (!bootstrapResult.ok) {
    return bootstrapResult;
  }
  const bootstrap = JSON.parse(bootstrapResult.messages[0]) as BootstrapPayload;

  let launch: AgentLaunchPayload | undefined;
  if (!options.dryRun) {
    launch = agentLaunchFromAssignments(repoPath, {
      agents,
      command: options.command,
      write: options.write,
      exec: options.exec,
      planned: bootstrap.agentPlan,
      fallbackToExisting: true,
    });
  }

  const payload = agentDispatchPayload(agents, bootstrap, launch);
  return agentDispatchResult(payload, options.json);
}

