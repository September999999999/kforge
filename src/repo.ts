function claimSourceDriftRefs(repoPath: string, claimCreated: string, sources: string[]): ClaimSourceDrift[] {
  const claimCreatedDate = dateOnlyFromString(claimCreated);
  if (!claimCreatedDate) {
    return [];
  }

  const drift: ClaimSourceDrift[] = [];
  for (const source of sources) {
    if (isExternalRef(source) || repoPathBoundaryError(repoPath, source)) {
      continue;
    }

    const sourcePath = path.resolve(repoPath, source);
    if (!exists(sourcePath) || !statSyncish(sourcePath).isFile()) {
      continue;
    }

    const sourceModified = localDateOnly(statSyncish(sourcePath).mtime);
    if (sourceModified > claimCreatedDate) {
      drift.push({ source, sourceModified, claimCreated: claimCreatedDate });
    }
  }
  return drift;
}

