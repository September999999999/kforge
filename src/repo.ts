export function initRepo(repoPath: string, options: InitOptions = {}): CommandResult {
  mkdirSyncish(repoPath);

  for (const directory of CANONICAL_DIRS) {
    const target = path.join(repoPath, directory);
    mkdirSyncish(target);
    writeIfMissing(path.join(target, ".gitkeep"), "", options.force);
  }

  writeIfMissing(path.join(repoPath, "kb.yaml"), kbYaml(), options.force);
  writeIfMissing(path.join(repoPath, "AGENTS.md"), repoAgents(), options.force);
  writeIfMissing(path.join(repoPath, "wiki", "Home.md"), homePage(), options.force);
  writeIfMissing(path.join(repoPath, "indexes", "brief.md"), briefPage(), options.force);

  const messages = [
    `Initialized knowledge repo at ${repoPath}`,
    "Created raw/, wiki/, claims/, indexes/, outputs/, reviews/, tasks/, runs/",
  ];

  if (options.example) {
    createExampleRepoContent(repoPath, options);
    refreshRepo(repoPath);
    messages.push("Created example raw, wiki, claim, review, output, indexes, and derived reports.");
    messages.push("Next: run `kforge workflow`, `kforge search --query provenance`, or open the repo in Obsidian.");
  } else {
    messages.push("Next: add source files under raw/ and run `kforge refresh`.");
  }

  return { ok: true, messages };
}

export function demoRepo(repoPath: string, options: DemoOptions = {}): CommandResult {
  const result = initRepo(repoPath, { ...options, example: true });
  if (!result.ok) {
    return result;
  }

  return {
    ok: true,
    messages: [
      ...result.messages,
      "",
      "Demo repo ready.",
      `Open: ${repoPath}`,
      "Try:",
      `  cd ${shellQuote(repoPath)}`,
      "  kforge context .",
      "  kforge review queue .",
      "  kforge doctor .",
    ],
  };
}

