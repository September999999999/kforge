  if (!searchable) {
    lines.push("This output is not a supported text type for structured inspection.", "");
  } else {
    const text = readText(file);
    const sourceRefsForFile = refsForFileSources(text, rel);
    lines.push("## Metadata", "");
    lines.push(`- title: ${documentTitle(text, rel)}`);
    pushOptionalField(lines, "status", metadataField(text, "status"));
    pushOptionalField(lines, "kind", metadataField(text, "kind"));
    pushOptionalField(lines, "created", metadataField(text, "created"));

    lines.push("", "## Headings", "");
    lines.push(...bulletList(markdownHeadings(text).slice(0, 12), "No headings found."));

    lines.push("", "## Sources Mentioned By Output", "");
    lines.push(...bulletList(sourceRefsForFile, "No source references found."));
  }

  lines.push("", "## Referenced As Source", "");
  lines.push(...bulletList(filesReferencingSource(repoPath, rel), "No local files reference this output as a source."));

