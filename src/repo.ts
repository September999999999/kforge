function trustScoreValue(repoPath: string): number | undefined {
  return trustScoreData(repoPath).trustScore;
}

function trustScoreData(repoPath: string): { metrics: ScoreMetric[]; openReviews: number; trustScore?: number } {
  const wikiFiles = iterFiles(path.join(repoPath, "wiki"));
  const scoredWikiFiles = wikiFiles.filter((file) => path.basename(file) !== "Home.md");
  const claimFiles = iterFiles(path.join(repoPath, "claims"));
  const reviewFiles = iterFiles(path.join(repoPath, "reviews"));
  const doctor = doctorRepo(repoPath);

  const wikiWithSources = scoredWikiFiles.filter((file) => frontmatterSourceRefs(readText(file)).length > 0).length;
  const claimsWithSources = claimFiles.filter((file) => sourceRefs(readText(file)).length > 0).length;
  const reviewsWithSources = reviewFiles.filter((file) => sourceRefs(readText(file)).length > 0).length;
  const reviewsWithTargets = reviewFiles.filter((file) => reviewTargetRefs(readText(file)).length > 0).length;
  const openReviews = reviewFiles.filter((file) => {
    const status = documentField(readText(file), "Status") ?? "unknown";
    return isOpenReviewStatus(status);
  }).length;
  const metrics = [
    metric("wiki provenance coverage", wikiWithSources, scoredWikiFiles.length),
    metric("claim provenance coverage", claimsWithSources, claimFiles.length),
    metric("review source coverage", reviewsWithSources, reviewFiles.length),
    metric("review target coverage", reviewsWithTargets, reviewFiles.length),
    metric("review debt clearance", reviewFiles.length - openReviews, reviewFiles.length),
    {
      label: "doctor health",
      value: doctor.ok ? 100 : 0,
      line: `- doctor health: ${doctor.ok ? "clean" : "needs attention"}`,
    },
  ];
  const scoredMetrics = metrics.filter((item) => item.value !== undefined);
  const trustScore =
    scoredMetrics.length === 0
      ? undefined
      : Math.round(scoredMetrics.reduce((sum, item) => sum + (item.value ?? 0), 0) / scoredMetrics.length);
  return { metrics, openReviews, trustScore };
}

interface ScoreMetric {
  label: string;
  value?: number;
  line: string;
}

function metric(label: string, numerator: number, denominator: number): ScoreMetric {
  if (denominator === 0) {
    return { label, value: undefined, line: `- ${label}: n/a (0/0)` };
  }

  const value = Math.round((numerator / denominator) * 100);
  return { label, value, line: `- ${label}: ${value}% (${numerator}/${denominator})` };
}

function searchScope(repoPath: string, scope: SearchScope, terms: string[]): SearchResultItem[] {
  return iterFiles(path.join(repoPath, scope))
    .filter(isSearchableFile)
    .map((file) => scoreFile(repoPath, scope, file, terms))
    .filter((result): result is SearchResultItem => result !== undefined);
}

function scoreFile(
  repoPath: string,
  scope: SearchScope,
  file: string,
  terms: string[],
): SearchResultItem | undefined {
  const rel = toRepoPath(repoPath, file);
  const text = readText(file);
  const haystack = `${rel}\n${text}`.toLowerCase();
  const matchedTerms = terms.filter((term) => haystack.includes(term));

  if (matchedTerms.length === 0) {
    return undefined;
  }

  const pathScore = occurrenceCount(rel.toLowerCase(), terms) * 8;
  const textScore = occurrenceCount(text.toLowerCase(), terms);
  const coverageScore = matchedTerms.length * 20;

  return {
    path: rel,
    scope,
    score: coverageScore + pathScore + textScore,
    snippet: bestSnippet(text, terms),
  };
}

function searchPayload(query: string, scopes: SearchScope[], limit: number, results: SearchResultItem[]): SearchPayload {
  return {
    ok: true,
    updatedAt: today(),
    query,
    scopes,
    limit,
    total: results.length,
    items: results,
    next:
      results.length > 0
        ? results.slice(0, 3).map((result) => `kforge inspect . --file ${shellQuote(result.path)}`)
        : ["kforge source list .", "kforge compile plan ."],
  };
}

