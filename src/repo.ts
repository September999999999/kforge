function exists(target: string): boolean {
  try {
    accessSyncish(target, constants.F_OK);
    return true;
  } catch {
    return false;
  }
}

function isDirectory(target: string): boolean {
  try {
    return statSyncish(target).isDirectory();
  } catch {
    return false;
  }
}

function readText(file: string): string {
  return readFileSyncish(file);
}

function writeIfMissing(file: string, content: string, force = false): void {
  if (force || !exists(file)) {
    mkdirSyncish(path.dirname(file));
    writeFileSyncish(file, content);
  }
}

