export function inspectSource(repoPath: string, options: SourceInspectOptions): CommandResult {
  requireRepo(repoPath);

  const fileRef = normalizeRepoRef(options.file);
  if (!fileRef) {
    return { ok: false, messages: ["source file is required"] };
  }

  if (!fileRef.startsWith("raw/") || fileRef.startsWith("raw/_meta/")) {
    return { ok: false, messages: [`source inspect can only inspect raw source files: ${fileRef}`] };
  }

  const invalidRefs = invalidRepoPathRefs(repoPath, "source inspect", [fileRef]);
  if (invalidRefs.length > 0) {
    return {
      ok: false,
      messages: ["source inspect path check failed:", ...invalidRefs.map((item) => `  - ${item}`)],
    };
  }

  const filePath = path.resolve(repoPath, fileRef);
  if (!exists(filePath)) {
    return { ok: false, messages: [`source file not found: ${fileRef}`] };
  }

  if (!statSyncish(filePath).isFile()) {
    return { ok: false, messages: [`source target is not a file: ${fileRef}`] };
  }

  return { ok: true, messages: [sourceInspect(repoPath, filePath)] };
}

