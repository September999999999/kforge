function countRecord(values: string[]): Record<string, number> {
  return Object.fromEntries([...countBy(values).entries()].sort(([left], [right]) => left.localeCompare(right)));
}

function recordLines(values: Record<string, number>): string[] {
  const entries = Object.entries(values);
  if (entries.length === 0) {
    return ["- none"];
  }
  return entries.map(([key, count]) => `- ${key}: ${count}`);
}

function bulletLines(values: string[], empty: string): string[] {
  if (values.length === 0) {
    return [`- ${empty}`];
  }
  return values.map((value) => `- ${value}`);
}

function countBy(values: string[]): Map<string, number> {
  const counts = new Map<string, number>();
  for (const value of values) {
    counts.set(value, (counts.get(value) ?? 0) + 1);
  }
  return counts;
}

function formatCounts(counts: Map<string, number>): string {
  return [...counts.entries()]
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([key, count]) => `${key}=${count}`)
    .join(", ");
}

