function compilePlanCandidates(repoPath: string): CompilePlanCandidate[] {
  const rawFiles = rawSourceFiles(repoPath);
  const wikiFiles = iterFiles(path.join(repoPath, "wiki"));
  return rawFiles.map((file) => compilePlanCandidate(repoPath, file, wikiFiles));
}

type CompilePlanCandidate = {
  source: string;
  title: string;
  target: string;
  status: "queued" | "covered";
  coveredBy: string[];
};

function compilePlanCandidate(repoPath: string, rawFile: string, wikiFiles: string[]): CompilePlanCandidate {
  const source = toRepoPath(repoPath, rawFile);
  const metadata = readRawSourceMetadata(repoPath, rawFile);
  const title = metadata?.title ?? sourceFallbackTitle(rawFile);
  const target = nextCompilePlanTarget(repoPath, title, wikiFiles);
  const coveredBy = wikiFiles
    .filter((file) => frontmatterSourceRefs(readText(file)).some((ref) => sameRepoRef(ref, source)))
    .map((file) => toRepoPath(repoPath, file))
    .sort();

  return {
    source,
    title,
    target,
    status: coveredBy.length > 0 ? "covered" : "queued",
    coveredBy,
  };
}

function nextCompilePlanTarget(repoPath: string, title: string, wikiFiles: string[]): string {
  const slug = slugify(title) || "article";
  const base = titleFromTarget(slug).replace(/\//g, " ").trim() || "Article";
  const existing = new Set(wikiFiles.map((file) => toRepoPath(repoPath, file)));
  let candidate = `wiki/${base}.md`;
  let counter = 2;

  while (existing.has(candidate)) {
    candidate = `wiki/${base} ${counter}.md`;
    counter += 1;
  }

  return candidate;
}

function compilePlanCommand(item: CompilePlanCandidate): string {
  return `kforge compile . --source ${shellQuote(item.source)} --target ${shellQuote(item.target)} --write`;
}

