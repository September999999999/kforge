function compileBrief(repoPath: string, options: CompileOptions): CommandResult {
  const sources = [...new Set(options.sources.map(normalizeRepoRef).filter(Boolean))];
  const target = normalizeRepoRef(options.target);
  const title = options.title?.trim() || titleFromTarget(target);

  if (!target) {
    return { ok: false, messages: ["compile target is required"] };
  }

  if (sources.length === 0) {
    return { ok: false, messages: ["at least one --source is required"] };
  }

  const invalidTarget = invalidRepoPathRefs(repoPath, "compile target", [target]);
  if (invalidTarget.length > 0) {
    return {
      ok: false,
      messages: ["compile target check failed:", ...invalidTarget.map((item) => `  - ${item}`)],
    };
  }

  const invalidSources = invalidRepoPathRefs(repoPath, "compile source", sources);
  if (invalidSources.length > 0) {
    return {
      ok: false,
      messages: ["compile source path check failed:", ...invalidSources.map((item) => `  - ${item}`)],
    };
  }

  const missingSources = missingSourceRefs(repoPath, "compile source", sources);
  if (missingSources.length > 0) {
    return {
      ok: false,
      messages: ["compile source check failed:", ...missingSources.map((item) => `  - ${item}`)],
    };
  }

  const sourceNotFiles = sources.filter((source) => !statSyncish(path.resolve(repoPath, source)).isFile());
  if (sourceNotFiles.length > 0) {
    return {
      ok: false,
      messages: ["compile sources must be files:", ...sourceNotFiles.map((item) => `  - ${item}`)],
    };
  }

  const targetPath = path.resolve(repoPath, target);
  if (exists(targetPath) && !statSyncish(targetPath).isFile()) {
    return { ok: false, messages: [`compile target is not a file: ${target}`] };
  }

  const targetExists = exists(targetPath);
  const targetSearchable = targetExists && isSearchableFile(targetPath);
  const targetExcerpt = targetSearchable ? trimForBrief(readText(targetPath), 6000) : undefined;
  const sourceExcerpts = sources.map((source) => sourceExcerpt(repoPath, source));
  const sourceLines = sources.map((source) => `  - ${source}`).join("\n");

  const lines = [
    GENERATED_HEADER,
    "# Compile Brief",
    "",
    `Title: ${title}`,
    `Target: \`${target}\``,
    `Updated: ${today()}`,
    "",
    "## Goal",
    "",
    `Compile the listed source material into a reviewable wiki article at \`${target}\`.`,
    "",
    "## Operating Contract",
    "",
    "- Treat `raw/` files as evidence, not editable draft material.",
    "- Keep source facts, synthesis, and uncertainty visibly separate.",
    "- Prefer a small focused article over a broad rewrite.",
    "- Use repo-local paths when citing evidence.",
    "- Create or update a review artifact before making broad or risky wiki edits.",
    "- Run `kforge index`, `kforge doctor`, and `kforge score` after applying changes.",
    "",
    "## Expected Wiki Shape",
    "",
    "```yaml",
    "---",
    `title: ${title}`,
    "status: draft",
    "kind: concept",
    "sources:",
    sourceLines,
    "confidence: medium",
    `last_compiled: ${today()}`,
    "---",
    "```",
    "",
    "## Sources",
    "",
    "| Source | Size | Embedded |",
    "| --- | ---: | --- |",
    ...sourceExcerpts.map((source) => {
      return `| \`${source.path}\` | ${source.size} bytes | ${source.embedded ? "yes" : "no"} |`;
    }),
    "",
    "## Target State",
    "",
    `- exists: ${targetExists ? "yes" : "no"}`,
    `- searchable text: ${targetSearchable ? "yes" : "no"}`,
    "",
  ];

  if (targetExcerpt) {
    lines.push("### Existing Target Excerpt", "", fencedBlock("markdown", targetExcerpt.text), "");
    if (targetExcerpt.truncated) {
      lines.push("- Existing target excerpt was truncated for the compile brief.", "");
    }
  } else if (targetExists) {
    lines.push("Existing target was not embedded because it is not a supported text file.", "");
  } else {
    lines.push("Target does not exist yet. Create it as a new wiki page if the source material supports it.", "");
  }

  lines.push("## Source Excerpts", "");
  for (const source of sourceExcerpts) {
    lines.push(`### \`${source.path}\``, "");
    if (source.excerpt) {
      lines.push(fencedBlock(source.language, source.excerpt.text), "");
      if (source.excerpt.truncated) {
        lines.push("- Source excerpt was truncated for the compile brief.", "");
      }
    } else {
      lines.push("This source was not embedded because it is not a supported text file.", "");
    }
  }

  lines.push(
    "## Compile Instructions",
    "",
    "1. Read the source excerpts first, then inspect full source files only if needed.",
    "2. Draft or update the target wiki page with the expected frontmatter.",
    "3. Preserve every listed source in the `sources:` field.",
    "4. Add wikilinks only for concepts that should become durable pages.",
    "5. Put durable assertions that need explicit traceability in `claims/`.",
    "6. Put uncertain merges, conflicts, or broad rewrites in `reviews/` before applying them.",
    "7. End by running `kforge index`, `kforge doctor`, and `kforge score`.",
    "",
    "## Review Prompt",
    "",
    "Before applying the compiled article, verify:",
    "",
    "- [ ] each factual claim is supported by a listed source",
    "- [ ] synthesis is labeled as synthesis, not source fact",
    "- [ ] unsupported speculation is removed or explicitly marked",
    "- [ ] target path and wikilinks are intentional",
    "",
  );

  return { ok: true, messages: [lines.join("\n")] };
}

