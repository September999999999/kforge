function kbYamlIssues(repoPath: string): string[] {
  const fields = parseSimpleYaml(readText(path.join(repoPath, "kb.yaml")));
  const issues: string[] = [];

  if (fields.get("protocol") !== "kforge") {
    issues.push("protocol must be `kforge`");
  }

  if (fields.get("version") !== "0.1") {
    issues.push("version must be `0.1`");
  }

  for (const directory of CANONICAL_DIRS) {
    const key = `directories.${directory}`;
    if (fields.get(key) !== directory) {
      issues.push(`${key} must be \`${directory}\``);
    }
  }

  return issues;
}

function parseSimpleYaml(text: string): Map<string, string> {
  const values = new Map<string, string>();
  const stack: Array<{ indent: number; key: string }> = [];

  for (const rawLine of text.split("\n")) {
    const withoutComment = rawLine.replace(/\s+#.*$/, "");
    if (!withoutComment.trim()) {
      continue;
    }

    const match = withoutComment.match(/^(\s*)([A-Za-z0-9_-]+):\s*(.*?)\s*$/);
    if (!match) {
      continue;
    }

    const indent = match[1].length;
    const key = match[2];
    const value = match[3];
    while (stack.length > 0 && stack[stack.length - 1].indent >= indent) {
      stack.pop();
    }

    const pathKey = [...stack.map((item) => item.key), key].join(".");
    if (value) {
      values.set(pathKey, unquoteYamlScalar(value));
    } else {
      stack.push({ indent, key });
    }
  }

  return values;
}

function unquoteYamlScalar(value: string): string {
  return value.trim().replace(/^["']|["']$/g, "");
}

function dateOnlyFromString(value: string): string | undefined {
  const date = value.match(/\d{4}-\d{2}-\d{2}/)?.[0];
  return date || undefined;
}

function localDateOnly(date: Date): string {
  const year = date.getFullYear();
  const month = `${date.getMonth() + 1}`.padStart(2, "0");
  const day = `${date.getDate()}`.padStart(2, "0");
  return `${year}-${month}-${day}`;
}

