interface HandoffState {
  rawFiles: string[];
  wikiFiles: string[];
  claimFiles: string[];
  outputFiles: string[];
  openReviews: string[];
  doctorOk: boolean;
  claimAudit: ClaimAuditData;
}

function handoffNextMoves(repoPath: string, state: HandoffState): string[] {
  if (!state.doctorOk) {
    return [
      "- Fix structural issues from `kforge doctor .` before making new durable edits.",
      "- Run `kforge index .` after fixing links, source references, or review files.",
    ];
  }

  if (state.claimAudit.counts.sourceDrift > 0) {
    return [
      "- Turn source drift warnings into stale review artifacts.",
      "- Run `kforge claim review-drift .`, then inspect the generated reviews.",
    ];
  }

  if (state.openReviews.length > 0) {
    const sampleReview = firstRepoRef(repoPath, state.openReviews) ?? "reviews/review.md";
    return [
      "- Resolve open review artifacts before creating more durable changes.",
      `- Start with \`kforge inspect . --file ${shellQuote(sampleReview)}\`.`,
    ];
  }

  return workflowNextMove(
    state.rawFiles,
    state.wikiFiles,
    state.claimFiles,
    state.openReviews,
    state.outputFiles,
    state.doctorOk,
  );
}

