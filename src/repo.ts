export async function fetchSources(repoPath: string, options: FetchSourcesOptions): Promise<CommandResult> {
  requireRepo(repoPath);

  const listOrError = sourceFetchListInput(options);
  if (!listOrError.ok) {
    return listOrError.result;
  }

  const { label, lines } = listOrError;
  const candidates = sourceFetchListCandidates(lines, options.titlePrefix);
  if (candidates.length === 0) {
    return { ok: false, messages: [`source URL list has no http or https URLs: ${label}`] };
  }

  if (options.dryRun) {
    const items = candidates.map((candidate) => sourceFetchListItem(candidate.url, "would_fetch", { title: candidate.title }));
    if (options.json) {
      return { ok: true, messages: [JSON.stringify(sourceFetchListPayload(label, items, true), null, 2)] };
    }
    return {
      ok: true,
      messages: [
        `Dry run: would fetch ${items.length} URL source(s) from ${label}`,
        ...items.map((item) => `- ${item.url}${item.title ? ` -> ${item.title}` : ""}`),
      ],
    };
  }

  const items: SourceFetchListItem[] = [];
  for (const candidate of candidates) {
    const fetched = await fetchUrlSource(candidate.url);
    if (!fetched.ok) {
      items.push(sourceFetchListItem(candidate.url, "failed", { title: candidate.title, error: fetched.messages.join("; ") }));
      continue;
    }

    const imported = importFetchedSource(repoPath, candidate.url, fetched, {
      title: candidate.title,
      author: options.author,
      date: options.date,
      license: options.license,
      note: sourceFetchListNote(label, candidate.index, options.note),
    });
    items.push(sourceFetchListItem(candidate.url, "fetched", { title: candidate.title, copied: imported, fetched }));
  }

  const payload = sourceFetchListPayload(label, items, false);
  if (options.json) {
    return { ok: true, messages: [JSON.stringify(payload, null, 2)] };
  }

  return {
    ok: true,
    messages: [
      `Fetched ${payload.counts.fetched} URL source(s) from ${label}`,
      ...(payload.counts.failed > 0 ? [`Failed ${payload.counts.failed} URL source(s).`] : []),
      ...items.map((item) =>
        item.action === "fetched"
          ? `- ${item.url} -> ${item.source} (metadata: ${item.metadata})`
          : `- ${item.url} -> failed: ${item.error}`,
      ),
      "Next: run `kforge refresh`, then inspect or compile from the fetched sources.",
    ],
  };
}

export function importSources(repoPath: string, options: ImportSourcesOptions): CommandResult {
  requireRepo(repoPath);

  const sourceDir = path.resolve(options.dir);
  if (!exists(sourceDir)) {
    return { ok: false, messages: [`source directory not found: ${options.dir}`] };
  }

  if (!statSyncish(sourceDir).isDirectory()) {
    return { ok: false, messages: [`source import path is not a directory: ${options.dir}`] };
  }

  if (isCanonicalRepoImportPath(repoPath, sourceDir)) {
    return {
      ok: false,
      messages: [
        `source import refuses to import from the repo root or a kforge canonical directory: ${toDisplayPath(repoPath, sourceDir)}`,
      ],
    };
  }

  const files = importableSourceFiles(sourceDir);
  const reservedRawPaths = new Set<string>();
  const reservedMetadataPaths = new Set<string>();
  const titlePrefix = options.titlePrefix?.trim();
  const plan = files.map((file) => {
    const title = importSourceTitle(sourceDir, file, titlePrefix);
    const parsed = path.parse(file);
    const extension = parsed.ext || ".txt";
    const slug = slugify(title) || slugify(parsed.name) || "source";
    const rawPath = nextAvailableRawPath(repoPath, slug, extension, reservedRawPaths);
    const metadataPath = nextAvailableRawMetadataPath(repoPath, rawPath, reservedMetadataPaths);
    reservedRawPaths.add(rawPath);
    reservedMetadataPaths.add(metadataPath);
    return {
      sourcePath: file,
      title,
      rawPath,
      metadataPath,
      relativePath: path.relative(sourceDir, file).split(path.sep).join("/"),
    };
  });

  if (options.dryRun) {
    if (options.json) {
      return {
        ok: true,
        messages: [
          JSON.stringify(sourceImportPayload(sourceDir, plan.map((item) => sourceImportItem(repoPath, item, "would_import")), true), null, 2),
        ],
      };
    }
    return {
      ok: true,
      messages: [
        `Dry run: would import ${plan.length} source file(s) from ${sourceDir}`,
        ...plan.map((item) => `- ${item.relativePath} -> ${toRepoPath(repoPath, item.rawPath)}`),
      ],
    };
  }

  const copied = plan.map((item) =>
    copySourceIntoRaw(repoPath, item.sourcePath, {
      title: item.title,
      url: options.url,
      author: options.author,
      date: options.date,
      license: options.license,
      note: importSourceNote(sourceDir, item.sourcePath, options.note),
      targetPath: item.rawPath,
      metadataPath: item.metadataPath,
    }),
  );

  if (options.json) {
    return {
      ok: true,
      messages: [
        JSON.stringify(sourceImportPayload(sourceDir, plan.map((item) => sourceImportItem(repoPath, item, "imported")), false), null, 2),
      ],
    };
  }

  return {
    ok: true,
    messages: [
      `Imported ${copied.length} source file(s) from ${sourceDir}`,
      ...copied.map((item) => `- ${item.rawRef} (metadata: ${item.metadataRef})`),
      "Next: run `kforge refresh`, then inspect or compile from the new raw sources.",
    ],
  };
}

